package ch02;

public class VarEx {
	public static void main(String[] args) {
		int year = 0, age = 14;
		System.out.println(year);
		System.out.println(age);
		
		year=age+2000; age=age+1;
		System.out.print(year);
		System.out.println(age);
		
		boolean power = true;
		System.out.println(power);
		
		float f1 = 3.14f;
		float f2 = 3.4f;
		
		System.out.println(f1);
		System.out.println(f2);
		
	}

}
