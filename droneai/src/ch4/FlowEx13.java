package ch4;


class FlowEx13 {
    public static void main(String[] args) {
        // 세로 출력
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        System.out.println(); // 줄 바꿈

        // 가로 출력
        for (int i = 1; i <= 5; i++) {
            System.out.print(i);
        }
    }
}
